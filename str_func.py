def upper_string():
    '''Получаем строку и выводим её заглавными буквами'''
    my_string = str(input())
    up_str = my_string.upper()
    return up_str

def title_string():
    '''Получаем строку и выводим все слова заглавными первыми буквами'''
    t_string = str(input())
    tit_string = t_string.title()
    return tit_string
